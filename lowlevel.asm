1. What are rbx, rax, ecx?
    rbx - Used to store data or memory addresses. Often used as a base pointer for memory access.
    rax - Used for arithmetic operations and to hold the return value of a function.
    ecx - Used for arithmetic operations and to hold the arguments of a function.

